class pbThead extends Thread {
    private JProgressBar pbar;

    pbThead(JProgressBar pbar) {
        this.pbar = pbar;
    }

    @Override
    public void run() {
        int min = 0;
        int max = 100;

        pbar.setMinimum(min);
        pbar.setMaximum(max);
        pbar.setValue(0);

        for (int i = min; i <= max; i++) {
            pbar.setValue(i);

            try {
                sleep(50); // Pause pour voir la progression
            } catch (InterruptedException ex) {
                ex.printStackTrace();
            }
        }

        // Ouvrir Home aprÃ¨s chargement
        SwingUtilities.invokeLater(() -> {
            new Home().setVisible(true);
            JFrame frame = (JFrame) SwingUtilities.getWindowAncestor(pbar);
            if (frame != null) frame.dispose();
        });
    }
}



