    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String email = jTextField1.getText();
    String password = String.valueOf(jPasswordField1.getPassword());

    // Vérifier si les champs sont remplis
    if (email.isEmpty() || password.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please fill in all fields", "Error", JOptionPane.WARNING_MESSAGE);
        return;
    }

    // Connexion à la base de données
    try {
        Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/event_management", "root", "");

        // Vérifier si l'utilisateur est admin ou user
        String sql = "SELECT role FROM users WHERE email = ? AND password = ?";
        PreparedStatement pst = con.prepareStatement(sql);
        pst.setString(1, email);
        pst.setString(2, password);
        ResultSet rs = pst.executeQuery();

        if (rs.next()) { // L'utilisateur existe
            String role = rs.getString("role"); // Récupère le rôle de l'utilisateur

            if (role.equals("admin")) {
                JOptionPane.showMessageDialog(this, "Welcome, Admin!");
                new adminpage().setVisible(true); // Afficher la page admin
            } else {
                JOptionPane.showMessageDialog(this, "Welcome, User!");
                new eventpageuser().setVisible(true); // Afficher la page utilisateur
            }

            this.dispose(); // Ferme la fenêtre de login après connexion

        } else {
            JOptionPane.showMessageDialog(this, "Invalid email or password", "Login Error", JOptionPane.ERROR_MESSAGE);
        }

        con.close();
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Database error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton2ActionPerformed

